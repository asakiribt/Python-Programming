import turtle
kalem=turtle.Turtle()
kalem.forward(100)   #100 piksel ilerle
kalem.backward(200) #200 piksel ilerle
kalem.right(45)     #100 45 derece sağa dön
kalem.forward(100)  # dönülen yöne 100 piksel ilerle
kalem.left(90)      #sola 90 derece dön
kalem.forward(100)  #dönülen yöne 100 piksel ilerle
turtle.done()